public class EvaluacionNumeros {
    public static int mayorDosNumeros(int num1, int num2){
        if (num1 > num2){
            return num1;
        }else{
            return num2;
        }
    }
    
    public static int menorDosNumeros(int num1, int num2){
        if (num1 < num2) {
            return num1;
        }else{
            return num2;
        }
    }
}
